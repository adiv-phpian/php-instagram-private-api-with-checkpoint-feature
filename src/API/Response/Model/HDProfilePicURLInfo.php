<?php

namespace Instagram\API\Response\Model;

class HDProfilePicURLInfo extends Model {

    /**
     * Url
     * @var string
     */
    protected $url;

    /**
     * Width
     * @var int
     */
    protected $width;

    /**
     * Height
     * @var int
     */
    protected $height;

    /**
     * @return string
     */
    public function getUrl()
    {
        return $this->url;
    }

    /**
     * @param string $url
     */
    public function setUrl($url)
    {
        $this->url = $url;
    }

    /**
     * @return int
     */
    public function getWidth()
    {
        return $this->width;
    }

    /**
     * @param int $width
     */
    public function setWidth($width)
    {
        $this->width = $width;
    }

    /**
     * @return int
     */
    public function getHeight()
    {
        return $this->height;
    }

    /**
     * @param int $height
     */
    public function setHeight($height)
    {
        $this->height = $height;
    }

}